#include <iostream>
#include <string>
using namespace std;
 
string reverse(string str){
	string temp = "";
	for(int i = str.size(); i > 0; i--){
		temp+= str[i - 1];
	}
	return temp;
}
 
int main()
{
	string str;
    cin >> str;
    while(cin >> str){
    	cout << reverse(str) << endl;
    }
    return 0;
}
